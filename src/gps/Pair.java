package gps;

/**
 * Class for storing the pair of integers.
 */
public class Pair {

	/* Properties */

	protected int a;
	protected int b;

	/* Methods */

	// constructor
	public Pair(int a, int b) {
		this.a = a;
		this.b = b;
	}

	// getter
	public int geta() {
		return this.a;
	}

	public int getb() {
		return this.b;
	}
}
